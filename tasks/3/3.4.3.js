// Найдите сумму элементов этого объекта.

let obj = {
	1: {
		1: 11,
		2: 12,
		3: 13,
	},
	2: {
		1: 21,
		2: 22,
		3: 23,
	},
	3: {
		1: 24,
		2: 25,
		3: 26,
	},
}

let sum = Object.values(obj).reduce((acc, value) => {
    Object.values(value).map((value) => {
        acc += value;
    })
    return acc;
}, 0);

